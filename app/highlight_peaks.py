from math import sqrt, atan2, pi

"""
Creates a list of Visible Peaks based on a Line of Sight Map
Contains a list of Munros and Corbetts in Scotland
"""


class VisiblePeak:
    """
    Visible Peak
    """
    def __init__(self, b, e, n, d, h, v):
        self.bearing = b
        self.elevation = e
        self.name = n
        self.distance = d
        self.height = h
        self.visible = v


class HighlightPeaks:
    """
    List of peaks to highlight on the map
    """
    MAX_RANGE = 50000

    def get_visible_peaks(self, los_map, x, y, h):
        """
        Return a list of visible peaks for a given x, y, h using a LineOfSightMap
        :param los_map:
        :param x:
        :param y:
        :param h:
        :return:
        """
        visible_peaks = []
        peaks = self._get_all_peaks()
        for peak in peaks:
            within_range, ground_dist = self._is_within_range(x, y, peak[1], peak[2], self.MAX_RANGE)
            if within_range:
                bearing = self._get_bearing(x, y, peak[1], peak[2])
                elevation = self._get_elevation(h, peak[0], ground_dist)
                # vpeak = VisiblePeak(bearing, elevation, peak[3], ground_dist, peak[0],
                #                    los_map.is_visible(bearing, elevation, ground_dist))
                # visible_peaks.append(vpeak)
                if los_map.is_visible(bearing, elevation, ground_dist):
                    visible_peak = VisiblePeak(bearing, elevation, peak[3], ground_dist, peak[0], True)
                    visible_peaks.append(visible_peak)
        return visible_peaks

    def save_to_file(self, peaks, peaks_filename):
        with open(peaks_filename, "w") as peaks_file:
            peaks_file.write("{\"peaks\":[")
            first = True
            for peak in peaks:
                if not first:
                    peaks_file.write(",\n")
                line = \
                    """
                        {{\"bearing\": {0},
                        \"elevation\": {1},
                        \"distance\": {2},
                        \"name\": \"{3}\",
                        \"height\": {4},
                        \"visible\": {5}}}
                    """.format(peak.bearing, peak.elevation, peak.distance, peak.name, peak.height,
                               "true" if peak.visible else "false")
                peaks_file.write(line)
                first = False
            peaks_file.write("]}")

    @staticmethod
    def _is_within_range(x1, y1, x2, y2, max_range):
        """
        Is a peak (x2, y2) within the max_range from the observation point (x1, y1). Pythagoras
        :param x1:
        :param y1:
        :param x2:
        :param y2:
        :param max_range:
        :return:
        """
        range2 = max_range * max_range
        dist = ((x2 - x1)*(x2 - x1)) + ((y2 - y1)*(y2 - y1))
        if dist < range2:
            return True, sqrt(dist)
        else:
            return False, 0

    @staticmethod
    def _get_bearing(x1, y1, x2, y2):
        """
        Return the direction angle to a peak (x2, y2) from an observation point (x1, y1)
        :param x1:
        :param y1:
        :param x2:
        :param y2:
        :return:
        """
        b = int(round(atan2(x2 - x1, y2 - y1) * 3200 / pi))
        b = b + 6400 if b < 0 else b
        return b

    @staticmethod
    def _get_elevation(h1, h2, ground_distance):
        """
        Get the elevation from a peak h2 from observation point h1 at ground distance ground_distance
        using same Mils approximation as the LineOfSightMap
        :param h1:
        :param h2:
        :param ground_distance:
        :return:
        """
        return ((h2 - h1) * 1000) // ground_distance

